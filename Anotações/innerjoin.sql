SELECT CLIENTE.NOME, CLIENTE.SEXO, ENDERECO.BAIRRO, ENDERECO.CIDADE, 
TELEFONE.TIPO, TELEFONE.NUMERO FROM CLIENTE
INNER JOIN ENDERECO
ON IDCLIENTE = IDENDERECO
INNER JOIN TELEFONE
ON IDCLIENTE = IDTELEFONE;



